package com.gndang.board.test.vo;


public class UserVO {
	private Integer userPid;
	private String userId;
	private String userPw;
	private String userPhon;
	private String userName;
	private String userAddr;
	
	public Integer getUserPid() {
		return userPid;
	}
	public void setUserPid(Integer userPid) {
		this.userPid = userPid;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPw() {
		return userPw;
	}
	public void setUserPw(String userPw) {
		this.userPw = userPw;
	}
	public String getUserPhon() {
		return userPhon;
	}
	public void setUserPhon(String userPhon) {
		this.userPhon = userPhon;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserAddr() {
		return userAddr;
	}
	public void setUserAddr(String userAddr) {
		this.userAddr = userAddr;
	}

}
